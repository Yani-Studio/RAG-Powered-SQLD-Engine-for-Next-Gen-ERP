import requests

def evaluate_semantic_correctness(question, sql, df_str, schema, model_name):
    """
    The Evaluator Agent. Looks at the question, the SQL, and the data result.
    Returns (True, "") if PASS, (False, "reason") if FAIL.
    """
    clean_model_name = model_name.split(' ')[0]
    
    system_prompt = f"""You are a strict Data Quality Evaluator Agent.
You are given a database schema, a user's question, the SQL query generated by another AI, and the resulting data (first 10 rows).
Your job is to determine if the SQL logically answers the user's question accurately.

Schema:
{schema}

CRITERIA for FAIL:
1. The question asked for a single value (e.g. "most popular"), but the result has multiple rows and missing LIMIT 1.
2. The question asked for a count, but the SQL doesn't have COUNT() or the result is empty.
3. The SQL is returning irrelevant columns.

Output Format:
If it is completely correct, output EXACTLY ONE WORD: PASS
If it is wrong, output FAIL: followed by the specific logical flaw. (e.g. "FAIL: You forgot to group by supplier_name")
"""
    prompt = f"Question: {question}\nSQL:\n{sql}\n\nData Result:\n{df_str}\n\nEvaluation:"
    
    url = "http://localhost:11434/api/generate"
    try:
        res = requests.post(url, json={"model": clean_model_name, "prompt": prompt, "system": system_prompt, "stream": False}, timeout=60)
        eval_result = res.json().get('response', '').strip()
        if eval_result.startswith('PASS'):
            return True, ""
        else:
            return False, eval_result.replace("FAIL:", "").strip()
    except Exception as e:
        return True, "" # Skip eval on error

def run_benchmark_grid(dataset, temperatures=[0.0, 0.3], models=["qwen2.5-coder:14b", "gemma2:9b"], max_samples=10):
    # For speed, sample the dataset
    if len(dataset) > max_samples:
        dataset = random.sample(dataset, max_samples)
        
    results = {
        "total_tested": len(dataset) * len(temperatures),
        "overall_accuracy": 0.0,
        "kr_accuracy": 0.0,
        "en_accuracy": 0.0,
        "syntax_error_rate": 0.0,
        "semantic_error_rate": 0.0,
        "recovery_rate": 0.0,
        "db_accuracy": {},
        "optimal_config": {}
    }
    
    # We will just test with the first available model locally, assumed to be llama3.1:8b or qwen2.5
    # Since we can't reliably know what's pulled, we'll try to find a working one.
    test_model = "llama3.1:8b" # Default fallback
    try:
        models_res = requests.get("http://localhost:11434/api/tags", timeout=5).json()
        available = [m['name'] for m in models_res['models']]
        if 'qwen2.5-coder:14b' in available:
            test_model = 'qwen2.5-coder:14b'
        elif 'gemma2:9b' in available:
            test_model = 'gemma2:9b'
        elif len(available) > 0:
            test_model = available[0]
    except:
        pass
    
    print(f"Starting Grid Search with model {test_model}...")
    
    best_temp = 0.0
    best_acc = 0.0
    
    global_stats = {"KR": {"pass": 0, "total": 0}, "EN": {"pass": 0, "total": 0}}
    db_stats = {}
    
    syntax_errors = 0
    semantic_errors = 0
    recoveries = 0
    
    start_time = time.time()
    
    for temp in temperatures:
        print(f"--- Testing Temperature: {temp} ---")
        temp_pass = 0
        
        for item in dataset:
            db_name = item['db_name']
            lang = item['language']
            q = item['question']
            
            if db_name not in db_stats:
                db_stats[db_name] = {"pass": 0, "total": 0}
            
            schema = llm_engine.SCHEMAS.get(db_name, "")
            
            # Generator
            sql = llm_engine.generate_sql(q, db_name, test_model, temperature=temp)
            
            # Dual Validation Loop
            success = False
            for attempt in range(3):
                try:
                    df = db_engine.execute_sql(db_name, sql)
                    df_str = df.head(10).to_markdown() if not df.empty else "EMPTY RESULT"
                    
                    # Evaluator
                    is_pass, reason = evaluate_semantic_correctness(q, sql, df_str, schema, test_model)
                    
                    if is_pass:
                        success = True
                        if attempt > 0:
                            recoveries += 1
                        break
                    else:
                        semantic_errors += 1
                        # Reflect to Generator
                        sql = llm_engine.generate_sql_with_self_correction(q, db_name, test_model, sql, f"Semantic Error: {reason}", temperature=temp)
                        
                except Exception as e:
                    syntax_errors += 1
                    sql = llm_engine.generate_sql_with_self_correction(q, db_name, test_model, sql, f"Syntax Error: {str(e)}", temperature=temp)
            
            # Stats update
            global_stats[lang]["total"] += 1
            db_stats[db_name]["total"] += 1
            if success:
                temp_pass += 1
                global_stats[lang]["pass"] += 1
                db_stats[db_name]["pass"] += 1
                
        temp_acc = temp_pass / len(dataset)
        print(f"Temperature {temp} Accuracy: {temp_acc*100:.1f}%")
        if temp_acc >= best_acc:
            best_acc = temp_acc
            best_temp = temp
            
    # Compile massive_results_ultimate.json
    total_runs = len(dataset) * len(temperatures)
    total_passes = global_stats["KR"]["pass"] + global_stats["EN"]["pass"]
    
    results["overall_accuracy"] = round((total_passes / total_runs) * 100, 2)
    results["kr_accuracy"] = round((global_stats["KR"]["pass"] / max(1, global_stats["KR"]["total"])) * 100, 2)
    results["en_accuracy"] = round((global_stats["EN"]["pass"] / max(1, global_stats["EN"]["total"])) * 100, 2)
    
    for db in db_stats:
        results["db_accuracy"][db] = round((db_stats[db]["pass"] / max(1, db_stats[db]["total"])) * 100, 2)
        
    results["syntax_error_rate"] = round((syntax_errors / total_runs) * 100, 2)
    results["semantic_error_rate"] = round((semantic_errors / total_runs) * 100, 2)
    results["recovery_rate"] = round((recoveries / max(1, (syntax_errors + semantic_errors))) * 100, 2)
    
    results["optimal_config"] = {"temperature": best_temp, "accuracy": round(best_acc * 100, 2)}
    results["execution_time_seconds"] = round(time.time() - start_time, 2)
    
    with open('massive_results_ultimate.json', 'w', encoding='utf-8') as f:
        json.dump(results, f, indent=4)
        
    print(f"Grid Search Complete! Optimal Temperature: {best_temp} with {best_acc*100:.1f}% Accuracy")
    print(f"Results saved to massive_results_ultimate.json")

if __name__ == "__main__":
    with open('benchmark_dataset.json', 'r', encoding='utf-8') as f:
        ds = json.load(f)
    run_benchmark_grid(ds)
